export function withPopularFallback(tracks, minimumTracks = 12) {
  if (tracks.length >= minimumTracks) return tracks

  const existing = new Set(
    tracks.map((track) => `${track.title || ''}-${track.artist_name || ''}`.toLowerCase())
  )

  const fallbackTracks = popularTracks.filter((track) => {
    const key = `${track.title}-${track.artist_name}`.toLowerCase()
    return !existing.has(key)
  })

  return [...tracks, ...fallbackTracks].sort((a, b) => (b.plays || 0) - (a.plays || 0))
}
